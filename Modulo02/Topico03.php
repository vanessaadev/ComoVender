                            <p class="SubTitulo"> Sobre a Nulidade dos Contratos Administrativos, é Possível?</p>
                            <hr>

                            <p>A nulidade do contrato ocorre quando é verificada a sua ilegalidade. A declaração de nulidade torna o contrato administrativo inexistente e invalida seus efeitos passados ou futuros.</p>
                            <p>A Administração tem o dever de indenizar o contratado pelo que ele tiver executado e pelos prejuízos devidamente comprovados que tenha sofrido até o momento em que a nulidade for declarada. Não cabe indenização quando ficar comprovada a responsabilidade do contratado por esses prejuízos.</p>
