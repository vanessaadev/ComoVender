                            <p class="SubTitulo"> É Possível Alterar os Contratos Administrativos?</p>
                            <hr>
                            <p>Quanto à alteração, os contratos administrativos podem ser alterados nos casos tipificados em lei. São exemplos de alteração: acréscimos ou supressões no objeto, prorrogações ou outras modificações admitidas em lei. Essas modificações são formalizadas por meio de instrumento usualmente denominado termo aditivo.</p>
                            <p>Por sua vez, quanto aos preços inicialmente pactuados nos contratos administrativos, estes podem ser adequados por meio de reajuste, repactuação e reequilíbrio contratual.</p>
                            <br>
                            <div class="alert alert-info">
                                <p>O reajuste é uma adequação de preços, sempre para maior, com periodicidade e índice pré-estabelecidos (índices de preços gerais, setoriais ou que reflitam a variação dos custos dos produtos ou dos insumos utilizados), somente admitido nos contratos com prazo de duração igual ou superior a um ano, desde que prevista no edital e no contrato. O reajuste não pode ocorrer antes de um ano e terá como data base a data limite para a apresentação da proposta ou do orçamento a que este se referir.</p>
                            </div>
                            <br>
                            <div class="alert alert-danger">
                                <p>A repactuação é uma forma de negociação entre a Administração e o contratado, que visa à adequação dos preços inicialmente pactuados aos novos preços do mercado. A repactuação somente pode ocorrer nos contratos que tenham por objeto a prestação de serviços de natureza continuada, em cujo termo de contrato exista cláusula admitindo a repactuação, que pode ser para aumentar ou diminuir o valor do contrato. Para a repactuação, deve ser apresentada demonstração analítica da variação dos custos dos componentes do contrato, devidamente justificada.</p>
                            </div>
                            <br>
                            <div class="alert alert-warning">
                                <p>O reequilíbrio é decorrente de fatos imprevisíveis, ou previsíveis, porém de consequências incalculáveis, que podem impedir ou retardar a execução do contrato, ou ainda, em caso de força maior, caso fortuito ou fato do príncipe. Tem por finalidade a manutenção do equilíbrio econômico-financeiro inicialmente contratado, para a justa remuneração da obra, dos serviços ou fornecimento. O reequilíbrio não tem data certa para ocorrer, não sendo possível, também, prefixar um índice.</p>
                            </div>
