                        <br><p class="SubTitulo"> Os Benefícios Trazidos pelo Estatuto das Micro e Pequenas Empresas</p>
                        <hr>

                        <p>O Governo do Estado instituiu o Estatuto do Microempreendedor Individual, da Microempresa e Empresa de Pequeno Porte por meio da Lei nº 15.306, de 08 de janeiro de 2013, em observância a Lei Complementar Federal nº 123, de 14 de dezembro de 2006, que assegura o tratamento favorecido, diferenciado e simplificado aos pequenos negócios nas compras da Administração Pública, criando assim mais oportunidades para estimular a economia local e gerar trabalho e renda.</p>

                        <div class=" bd-callout bd-callout-warning">
                            <p style="font-weight: 400;">De acordo com o artigo 3º da Lei Complementar nº 123/2006, consideram-se Microempresas (ME) ou Empresas de Pequeno Porte (EPP) a sociedade empresária, a sociedade simples e o empresário individual devidamente registrados no Registro de Empresas Mercantis (Junta Comercial) ou no Registro Civil de Pessoas Jurídicas, conforme o caso, desde que:</p>

                            <ul class="none">
                                <li><i class="fa fa-bullseye CorVerdeClaroEstado"></i> No caso de Microempresa (ME): aufira, em cada ano-calendário, receita bruta igual ou inferior a R$ 360.000,00;</li>
                                <li><i class="fa fa-bullseye CorVerdeClaroEstado"></i> No caso de Empresa de Pequeno Porte (EPP): aufira, em cada ano-calendário, receita bruta superior a R$ 360.000,00 e igual ou inferior a R$ 4.800.000,00.</li>
                            </ul>
                        </div>

                        <p>O acesso aos mercados pelas MPEs é tratado no capítulo V, tanto na Lei Federal Complementar nº 123/2006 como na Lei Estadual nº 15.306/2013.</p>
                        <p>É importante destacar a preocupação do legislador com a aplicação imediata da Lei Complementar nº 123/206, ao estabelecer a seguinte redação no parágrafo único do art. 47: “no que diz respeito às compras públicas, enquanto não sobrevier legislação estadual, municipal ou regulamento específico de cada órgão mais favorável à microempresa e empresa de pequeno porte, aplica-se a legislação federal”. No entanto, há dispositivos que ainda requerem regulamentação para serem aplicados, a exemplo do § 3º do art. 48 da referida lei.</p>


