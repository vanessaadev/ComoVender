                            <p>O Governo do Estado do Ceará, assim como os demais entes no âmbito dos Poderes da União, dos Estados, do Distrito Federal e dos Municípios, submete suas licitações e contratações diretas aos ditames da Lei nº 8.666, de junho de 1993 (lei das licitações e contratos administrativos) e, ainda, aos da Lei nº 10.520, de 17 de julho de 2002 (lei do pregão), neste caso, para aquisições de bens e serviços comuns.</p>
