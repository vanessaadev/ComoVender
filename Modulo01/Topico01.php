                        <p class="Titulo"> As Compras Governamentais Como Oportunidade Para os Pequenos Negócios</p>
                        <hr>

                        <div class="bd-callout bd-callout-success">
                            <p><strong>Objetivo:</strong> Apresentar as compras públicas como oportunidade de negócio para os microempreendedores individuais (MEIs), as microempresas (ME) e as empresas de pequeno porte (EPPs).</p>
                        </div>

                        <p>Com a promulgação da Constituição Federal de 1988, as compras governamentais ganharam nova dimensão na implementação das políticas públicas, uma vez que um dos alicerces do novo ordenamento jurídico-constitucional é a ideia de função social. Nesse sentido, as políticas públicas de compras passaram a ser compreendidas, também, como atividades-fim do Estado.</p>
                        <p>De longe, a Administração Pública é o maior comprador do país, razão pela qual é socialmente justo utilizar o poder de compra da União, dos Estados e dos Municípios para cumprir a função de gerar trabalho e renda, ainda que de forma indireta, para uma parte significativa da população, contribuindo, assim, para o desenvolvimento econômico e social.</p>
                        <p>As compras governamentais são regidas, em grande parte, pela Lei nº 8.666/1993, também conhecida como a Lei das Licitações e Contratos da Administração Pública. Recentemente entrou em vigor a Lei nº 14.133, de 1º de abril de 2021, que atualiza a Lei de Licitações e Contratos Administrativos, cuja utilização poderá ser opcional à Lei nº 8.666/1993 no período de até dois anos, a partir da data de sua publicação. Essas normas legais exigem a observância ao princípio da isonomia ou igualdade de condições entre os interessados nas compras governamentais. Porém, não é razoável que microempresas (MEs), empresas de pequeno porte (EPPs) e microempreendedores individuais (MEIs) concorram em igualdade de condições com empresas de grande porte. Nesse caso, recomenda-se adotar o princípio da equidade, ou seja, tratar os desiguais na medida de suas desigualdades.</p>
                        <p>Nesse sentido, a Constituição Federal de 1988 também assegura que a União, os Estados, o Distrito Federal e os Municípios deverão dispensar tratamento diferenciado e favorecido aos pequenos negócios, por meio da simplificação de suas obrigações administrativas, tributárias, previdenciárias e creditícias, ou pela eliminação ou redução destas por meio de lei. Essas conquistas estão se tornando possíveis graças às leis federais nº 123/2006 (Estatuto da Micro e Pequena Empresa – MPE) e nº 128/2008 (Microempreendedor Individual - MEI).</p>
                        <p>Com a implementação dessas normas legais, a União, os Estados, o Distrito Federal e os Municípios passaram a contar com um importante instrumento de fortalecimento da política de compras governamentais, por meio do tratamento diferenciado e favorecido dispensado aos pequenos negócios, contribuindo, assim, para a efetividade da função social que ao poder público cabe cumprir.</p>
                        <p>De acordo com o Serviço Brasileiro de Apoio às Micro e Pequenas Empresas – SEBRAE (2012), no Brasil, os pequenos negócios representam 99% dos estabelecimentos formais e empregam 52% dos trabalhadores com carteira assinada. Por outro lado, eles participam com apenas 28% do faturamento e 27% do Produto Interno Bruto (PIB), que representa o total das riquezas do país.</p>
                        <p>De acordo com o Painel das Compras do governo federal, só em 2019, as compras realizadas de microempresas e empresas de pequeno porte, por meio do portal Comprasgovernamentais, movimentaram mais de 400 bilhões de reais, oferecendo enormes oportunidades de negócios, quer seja pelo volume quer seja pela diversidade de produtos e serviços contratados por órgãos e entidades dos três níveis de governo e poderes do Estado.</p>
                        <p>Atualmente, os governos federal, estaduais e municipais, assim como as entidades de apoio às micro e pequenas empresas realizam esforços para ampliar a participação desses pequenos negócios nas compras governamentais, por meio de capacitação de agentes públicos e fornecedores, implantação de programas de compras, regulamentação da legislação etc.</p>
                        <p>Não é difícil imaginar a grande quantidade de produtos e serviços que os órgãos e entidades da administração pública contratam diretamente ou por meio de licitação, tais como: material de expediente, de higiene e de limpeza; descartáveis; utilidades; eletrônicos; eletrodomésticos; mobiliário; impressos; água, café, açúcar e outros gêneros alimentícios; uniformes; merenda escolar; carteiras escolares; serviços de impressão, de manutenção, de limpeza, de transporte, entre tantos outros.</p>
                        <p>Nesse contexto, as iniciativas de fomento às compras governamentais favorecem a formalização e o fortalecimento dos pequenos negócios e se constituem estratégias importantes para o desenvolvimento econômico e social, uma vez que contribuem para o dinamismo da economia local, gerando emprego e renda.</p>
                        <p>No caso do Estado do Ceará, o governo estabeleceu, entre outras iniciativas, uma política pública de fomento aos pequenos negócios, por meio do Estatuto do Microempreendedor Individual, da Microempresa e Empresa de Pequeno Porte.</p>

